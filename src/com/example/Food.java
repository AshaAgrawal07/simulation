package com.example;

public class Food {
    private String name;
    private String description;
    private String consequence;
    private int cost;
    private int quantity;

    public String getName() {
        return name;
    }

    public String getDescription() {
        return description;
    }

    public String getConsequence() {
        return consequence;
    }

    public int getCost() {
        return cost;
    }

    public int getQuantity() {
        return quantity;
    }
}
